    {
        if (PanelSetting::count() == 0) {
            PanelSetting::create(PanelSetting::DEFAULT_SETTING);
        }
        $data = [
            'offices' => Office::with('user')->get()->sortBy("user.name"),
            'states' => $this->getStates(),
            'regions' => Region::all(),
            'panelSetting' => PanelSetting::first()
        ];

        return view('inventory.index', $data);
    }

